package dataobject;

/**
 * @author Aditya
 *
 */
public class Response {
	
	String ddcName;
	String sysRowId;
	String objectId;
	String href;
	/**
	 * @return the ddcName
	 */
	public String getDdcName() {
		return ddcName;
	}
	/**
	 * @param ddcName the ddcName to set
	 */
	public void setDdcName(String ddcName) {
		this.ddcName = ddcName;
	}
	/**
	 * @return the sysRowId
	 */
	public String getSysRowId() {
		return sysRowId;
	}
	/**
	 * @param sysRowId the sysRowId to set
	 */
	public void setSysRowId(String sysRowId) {
		this.sysRowId = sysRowId;
	}
	/**
	 * @return the objectId
	 */
	public String getObjectId() {
		return objectId;
	}
	/**
	 * @param objectId the objectId to set
	 */
	public void setObjectId(String objectId) {
		this.objectId = objectId;
	}
	/**
	 * @return the href
	 */
	public String getHref() {
		return href;
	}
	/**
	 * @param href the href to set
	 */
	public void setHref(String href) {
		this.href = href;
	}
	
	

}
